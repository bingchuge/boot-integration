-- ----------------------------
-- 1、用户信息表
-- ----------------------------
drop table if exists sys_user;
create table sys_user
(
    user_id         bigint(20) not null auto_increment comment '用户ID',
    user_name       varchar(30)  default '' comment '用户昵称',
    user_type       varchar(2)   default '00' comment '用户类型（00系统用户 01注册用户）',
    name            varchar(32)  default '' comment '姓名',
    email           varchar(50)  default '' comment '用户邮箱',
    phone_number    varchar(11)  default '' comment '手机号码',
    sex             tinyint(1)   default '0' comment '用户性别（0男 1女 2未知）',
    avatar          varchar(100) default '' comment '头像路径',
    password        varchar(50)  default '' comment '密码',
    salt            varchar(20)  default '' comment '盐加密',
    status          tinyint(1)   default '0' comment '帐号状态（0正常 1停用）',
    login_date      datetime comment '最后登录时间',
    pwd_update_date datetime comment '密码最后更新时间',
    create_by       varchar(64)  default '' comment '创建者',
    create_time     datetime comment '创建时间',
    update_by       varchar(64)  default '' comment '更新者',
    update_time     datetime comment '更新时间',
    remark          varchar(500) default null comment '备注',
    del_flag        tinyint(1)   default '0' comment '删除标志（0代表存在 1代表删除）',
    primary key (user_id)
) engine = innodb
  auto_increment = 100 comment = '用户表';


-- ----------------------------
-- 2. 部门表
-- ----------------------------
drop table if exists sys_dept;
CREATE TABLE `sys_dept`
(
    `dept_id`     varchar(32) NOT NULL COMMENT '部门id',
    `parent_id`   bigint      DEFAULT '0' COMMENT '父部门id',
    `dept_name`   varchar(30) DEFAULT '' COMMENT '部门名称',
    `order_num`   int         DEFAULT '0' COMMENT '显示顺序',
    `status`      tinyint(1)  DEFAULT '1' COMMENT '部门状态（1正常 0停用）',
    `create_by`   varchar(64) DEFAULT '' COMMENT '创建者',
    `create_time` datetime    DEFAULT NULL COMMENT '创建时间',
    `update_by`   varchar(64) DEFAULT '' COMMENT '更新者',
    `update_time` datetime    DEFAULT NULL COMMENT '更新时间',
    `del_flag`    tinyint(1)  DEFAULT 0 COMMENT '删除标志（0代表存在 1代表删除）',
    PRIMARY KEY (`dept_id`)
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4
  COLLATE = utf8mb4_general_ci COMMENT ='部门表';


